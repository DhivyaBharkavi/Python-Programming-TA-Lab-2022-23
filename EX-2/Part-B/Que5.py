"""
Write a program to read two complex numbers from the user and find their sum, difference and product.
"""
c1=complex(input("Enter your first complex number "))
c2=complex(input("Enter your second complex number "))
print("Sum of two complex number is ",c1+c2)
print("Difference of two complex number is ",c1-c2)
print("Product of two complex number is ",c1*c2)
